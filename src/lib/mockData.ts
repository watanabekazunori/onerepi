export const findRecipesWithSharedIngredients = (baseRecipe: Recipe): Recipe[] => {
  const baseIngredients = new Set(
    baseRecipe.ingredients
      .filter(i => i.category === 'protein' || i.category === 'vegetable')
      .map(i => i.name)
  );

  return MOCK_RECIPES
    .filter(r => r.id !== baseRecipe.id)
    .map(r => ({
      recipe: r,
      sharedCount: r.ingredients.filter(i => baseIngredients.has(i.name)).length,
    }))
    .filter(({ sharedCount }) => sharedCount > 0)
    .sort((a, b) => b.sharedCount - a.sharedCount)
    .map(({ recipe }) => recipe);
};

// è¤‡æ•°ãƒ¬ã‚·ãƒ”é–“ã®å…±é€šé£Ÿæã‚’å–å¾—
export const getSharedIngredientsBetweenRecipes = (recipes: Recipe[]): string[] => {
  const ingredientCount: Record<string, number> = {};

  recipes.forEach(recipe => {
    recipe.ingredients
      .filter(i => i.category === 'protein' || i.category === 'vegetable')
      .forEach(i => {
        ingredientCount[i.name] = (ingredientCount[i.name] || 0) + 1;
      });
  });

  return Object.entries(ingredientCount)
    .filter(([_, count]) => count >= 2)
    .sort((a, b) => b[1] - a[1])
    .map(([name]) => name);
};
